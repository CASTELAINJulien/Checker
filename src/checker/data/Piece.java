package checker.data;

public class Piece {
	
	private int position;
	private String color;
	
	public Piece() {
		
	}
	public int getPosition() {
		return position;
	}
	public void setPosition(int position) {
		this.position = position;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
}
