package checker.panels;

public class Pause {
	private boolean capitulate;
	private boolean soundActivation;
	public Pause() {
		
	}
	public boolean isCapitulate() {
		return capitulate;
	}
	public void setCapitulate(boolean capitulate) {
		this.capitulate = capitulate;
	}
	public boolean isSoundActivation() {
		return soundActivation;
	}
	public void setSoundActivation(boolean soundActivation) {
		this.soundActivation = soundActivation;
	}
}
